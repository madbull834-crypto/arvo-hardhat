/**
 * Sync frontend/config.js from a deployment manifest.
 *
 * Usage:
 *   node scripts/sync_frontend_config.js sepolia
 *   NETWORK=sepolia node scripts/sync_frontend_config.js
 */
const fs = require("fs");
const path = require("path");

require("dotenv").config({ path: path.join(__dirname, "..", ".env") });

const NETWORKS = {
  sepolia: {
    chainId: 11155111,
    chainName: "Sepolia",
    rpcEnv: "SEPOLIA_RPC_URL",
    fallbackRpc: "https://eth-sepolia.g.alchemy.com/v2/YOUR_KEY",
    nativeCurrency: { name: "Sepolia ETH", symbol: "ETH", decimals: 18 },
    blockExplorerUrl: "https://sepolia.etherscan.io",
    title: "Sepolia Testnet",
  },
  bscTestnet: {
    chainId: 97,
    chainName: "BNB Smart Chain Testnet",
    rpcEnv: "BSC_TESTNET_RPC",
    fallbackRpc: "https://data-seed-prebsc-1-s1.binance.org:8545/",
    nativeCurrency: { name: "tBNB", symbol: "tBNB", decimals: 18 },
    blockExplorerUrl: "https://testnet.bscscan.com",
    title: "BSC Testnet",
  },
  bscMainnet: {
    chainId: 56,
    chainName: "BNB Smart Chain",
    rpcEnv: "BSC_MAINNET_RPC",
    fallbackRpc: "https://bsc-dataseed.binance.org/",
    nativeCurrency: { name: "BNB", symbol: "BNB", decimals: 18 },
    blockExplorerUrl: "https://bscscan.com",
    title: "BSC Mainnet",
  },
};

function readJson(file) {
  return JSON.parse(fs.readFileSync(file, "utf8"));
}

function requireAddress(manifest, key) {
  const value = manifest?.contracts?.[key];
  if (!value) throw new Error(`deployments/${network}.json is missing contracts.${key}`);
  return value;
}

const network = process.argv[2] || process.env.NETWORK || "sepolia";
const meta = NETWORKS[network];
if (!meta) {
  throw new Error(`Unsupported network "${network}". Use one of: ${Object.keys(NETWORKS).join(", ")}`);
}

const manifestPath = path.join(__dirname, "..", "deployments", `${network}.json`);
if (!fs.existsSync(manifestPath)) {
  throw new Error(`Deployment manifest not found: ${manifestPath}`);
}

const manifest = readJson(manifestPath);
const configPath = path.join(__dirname, "..", "frontend", "config.js");
const rpcUrl = process.env[meta.rpcEnv] || meta.fallbackRpc;
const oraclePair = manifest?.contracts?.oraclePair || manifest?.oracle?.pair || "";
const oracle = manifest?.oracle || {};

const config = `/**
 * ARVO Frontend Configuration — ${meta.title}
 *
 * Auto-generated by scripts/sync_frontend_config.js at ${new Date().toISOString()}
 * Source: deployments/${network}.json
 *
 * Do not put private keys in this file. It is served to browsers.
 */
window.ARVO_CONFIG = {
  appName: "Arvo",
  chainId: ${meta.chainId},
  chainName: "${meta.chainName}",
  rpcUrl: "${rpcUrl}",
  nativeCurrency: ${JSON.stringify(meta.nativeCurrency, null, 4).replace(/^/gm, "  ").trim()},
  blockExplorerUrl: "${meta.blockExplorerUrl}",
  logsApiPath: "/api/logs",
  eventStartBlock: ${Number(manifest.deployBlock || 0)},

  contracts: {
    usdt:       "${requireAddress(manifest, "usdt")}",
    orbd:       "${requireAddress(manifest, "orbd")}",
    oraclePair: "${oraclePair}",
    weeklyPool: "${requireAddress(manifest, "weeklyPool")}",
    matrix:     "${requireAddress(manifest, "matrix")}",
    genesis:    "${manifest.genesis || ""}"
  },

  oracle: {
    pair: "${oraclePair}",
    minTwapInterval: ${Number(oracle.minTwapInterval || 0)},
    maxAge: ${Number(oracle.maxAge || 0)},
    maxRateChangeBps: ${Number(oracle.maxRateChangeBps || 0)}
  }
};
`;

fs.writeFileSync(configPath, config);
console.log(`frontend/config.js synced from deployments/${network}.json`);
